/* ============================================================
   Interactive embed: MapLibre map + D3 popup + D3 legend.
   ============================================================ */

const ALL_YEARS = [1961, 1971, 1981, 1991, 2001, 2011, 2021, 2024];

// 10 fixed bins (slider-period agnostic).
const PCT_BINS = [-25, -15, -8, -3, 0, 5, 15, 35, 75];
const ABS_BINS = [-20000, -10000, -5000, -1000, 0, 1000, 5000, 10000, 20000];

// Discrete diverging palette — 10 colours (5 red + 5 green, skipping the
// cream centre so 0 is a clean red→green boundary).
const COLORS = [
  "#d46780", // 0 darkest red   — ≤−80% / ≤−20k
  "#df91a3", // 1
  "#e8acb3", // 2
  "#f0c6c3", // 3
  "#f7e1d4", // 4 lightest red  — −20→0 / −1k→0
  "#e7e7c3", // 5 lightest green— 0→+50 / 0→+1k
  "#d0d3a2", // 6
  "#bac082", // 7
  "#8e9847", // 8
  "#646c1d", // 9 darkest green — ≥+500% / ≥+20k
];

const NO_DATA_COLOR = "rgba(0,0,0,0)";    // transparent → basemap shows through

// ---- State ----------------------------------------------------------------
let yearA = 1961;
let yearB = 2024;
let mode = "pct";
let map;

// ---- Helpers --------------------------------------------------------------
function binIndex(value, bins) {
  if (value == null || !isFinite(value)) return null;
  for (let i = 0; i < bins.length; i++) {
    if (value < bins[i]) return i;
  }
  return bins.length;
}

// Walk forward (+1) or backward (-1) from `requested` until we find a year
// that this feature has data for. Used for the popup info-sentence so the
// UK (no 2024 data) reads as "between 1961 and 2021".
function effectiveYear(props, requested, direction) {
  const k = "pop_" + requested;
  if (props[k] != null) return [requested, props[k]];
  const range = direction < 0
    ? ALL_YEARS.filter(y => y < requested).reverse()
    : ALL_YEARS.filter(y => y > requested);
  for (const y of range) {
    if (props["pop_" + y] != null) return [y, props["pop_" + y]];
  }
  return [requested, null];
}

function formatAbsLabel(v) {
  const sign = v > 0 ? "+" : (v < 0 ? "−" : "");
  const abs = Math.abs(v);
  if (abs >= 1000) return sign + (abs / 1000) + "k";
  return sign + abs;
}

// ---- Paint expression for the choropleth ---------------------------------
// MVT properties carry the full pop_1961…pop_2024 series, so the fill
// colour is computed entirely in the paint expression — one
// setPaintProperty call per slider/mode change, not a per-LAU loop.
//
// For UK gemeenten pop_2024 is null (the JRC dataset stops at 2021 for
// the UK). When yearB=2024 we coalesce to pop_2021 so the UK still gets
// a colour. All other years are straight property reads.
function getPopExpr(year) {
  if (year === 2024) {
    return ["coalesce", ["get", "pop_2024"], ["get", "pop_2021"]];
  }
  return ["get", "pop_" + year];
}

function buildFillExpr(yA, yB, modeStr) {
  const bins = modeStr === "pct" ? PCT_BINS : ABS_BINS;
  const popA = getPopExpr(yA);
  const popB = getPopExpr(yB);
  const valExpr = modeStr === "pct"
    ? ["*", 100, ["/", ["-", popB, popA], popA]]
    : ["-", popB, popA];
  return [
    "case",
    ["any",
      ["==", popA, null],
      ["==", popB, null],
      ["==", popA, 0],
    ], NO_DATA_COLOR,
    [
      "step", valExpr,
      COLORS[0],
      bins[0], COLORS[1],
      bins[1], COLORS[2],
      bins[2], COLORS[3],
      bins[3], COLORS[4],
      bins[4], COLORS[5],
      bins[5], COLORS[6],
      bins[6], COLORS[7],
      bins[7], COLORS[8],
      bins[8], COLORS[9],
    ],
  ];
}

